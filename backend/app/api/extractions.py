from __future__ import annotations

from fastapi import APIRouter, Depends, HTTPException
from sqlalchemy import select
from sqlalchemy.orm import Session, selectinload

from ..ai.controls import utcnow
from ..db import get_db
from ..models import AuditLog, Correction, Document, DocumentStatus, Extraction
from ..schemas import AcceptExtractionIn, AddExtractionIn, CorrectionIn
from ..services.review_service import (
    mark_extraction_accepted,
    refresh_document_review_status,
)
